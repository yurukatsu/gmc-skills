export type Lang = 'en' | 'ja';

export const ALL_LANGS: Lang[] = ['en', 'ja'];

export function parseLang(input: string | undefined | null): Lang {
  if (!input) return 'en';
  const lower = input.toLowerCase();
  if (lower.startsWith('ja')) return 'ja';
  return 'en';
}

